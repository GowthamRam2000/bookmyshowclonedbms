    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here
        try {
                Class.forName("java.sql.Driver");
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/Bookmyshow","root","");
                Statement stmt=con.createStatement();
                String query="SELECT * FROM USERNAME WHERE Id=1;";              
                ResultSet rs=stmt.executeQuery(query);
                while(rs.next()) {
                    String name=rs.getString("Name");
                    jPanel1.setBorder(javax.swing.BorderFactory.createTitledBorder(null, "["+name+"]", javax.swing.border.TitledBorder.DEFAULT_JUSTIFICATION, javax.swing.border.TitledBorder.DEFAULT_POSITION, new java.awt.Font("Tahoma", 0, 12), new java.awt.Color(153, 0, 0))); // NOI18N
                }
            }
            catch(ClassNotFoundException | SQLException e) {
                JOptionPane.showMessageDialog(null,e);
            }
        //Loading user info
        try {                                                                    
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/Bookmyshow","root","");
            Statement stmt=con.createStatement();
            String query="SELECT * FROM SUBMISSION;";
            ResultSet rs=stmt.executeQuery(query);                              
            while(rs.next()) {
                String movie=rs.getString("Movie_Name");                        
                movie_name.setText(movie);
                String venues=rs.getString("Venue");
                movie_hall.setText(venues);                                     
                String seats=rs.getString("Seats");
                numberof_seats.setText(seats);
                String dates=rs.getString("Date");
                date.setText(dates);
                String time=rs.getString("Timing");
                timing.setText(time);
                String total_amt=rs.getString("Total_Price");
                amt.setText(total_amt);
                this.jLabel9.setText(Integer.toString(bid));
            }
            rs.close();                                                         
            stmt.close();
            con.close();
            
            Container c = getContentPane();
            BufferedImage im = new BufferedImage(c.getWidth(), c.getHeight(), BufferedImage.TYPE_INT_ARGB);
            c.paint(im.getGraphics());
            ImageIO.write(im, "PNG", new File("shot.png"));
        }
        catch(ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null,e);
        } catch (IOException ex) {
            Logger.getLogger(Payment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

